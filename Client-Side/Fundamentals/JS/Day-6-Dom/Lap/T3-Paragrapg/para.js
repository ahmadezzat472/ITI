var paragraph = document.getElementById("PAR");

function ChangeFont(x) {
  paragraph.style.fontFamily = x;
}

function ChangeAlign(x) {
  paragraph.style.textAlign = x;
}

function ChangeHeight(x) {
  paragraph.style.lineHeight = x;
}

function ChangeLSpace(x) {
  paragraph.style.letterSpacing = x + "px";
}

function ChangeIndent(x) {
  paragraph.style.textIndent = x + "px";
}

function ChangeTransform(x) {
  paragraph.style.textTransform = x;
}

function ChangeDecorate(x) {
  paragraph.style.textDecoration = x;
}

function ChangeBorder(x) {
  paragraph.style.borderStyle = x;
}
function ChangeBorderColor(x) {
  paragraph.style.borderColor = x;
}
